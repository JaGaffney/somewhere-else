  getLevelFromExp(exp: number): number {
    let lvl = 1
    for (const i in this.level) {
      if (exp > this.level[i]) {
        lvl = parseInt(i)
      }
    }
    return lvl
  }

  getNextLevelFromExp(exp: number): number {
    let currentLevel = this.getLevelFromExp(exp)
    let val = currentLevel + 1
    if (val < 100) {
      return this.level[val]
    } else {
      return this.level[99]
    }
  }
